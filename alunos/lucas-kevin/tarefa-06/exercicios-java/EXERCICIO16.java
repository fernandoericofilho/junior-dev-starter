public class EXERCICIO16 {
    public static void main(String[] args) {
        System.out.println("Imprimindo os números de 1 a 100:");

        // O laço 'for' tem 3 partes:
        // 1. Inicialização: int i = 1; (cria um contador 'i' começando em 1)
        // 2. Condição: i <= 100; (o laço continua enquanto 'i' for menor ou igual a 100)
        // 3. Incremento: i++; (a cada volta, 'i' aumenta em 1)
        for (int i = 1; i <= 100; i++) {
            System.out.println(i);
        }
    }
}