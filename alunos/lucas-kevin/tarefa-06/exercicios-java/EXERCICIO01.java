public class EXERCICIO01 {
    public static void main(String[] args) {
        // Declaração e atribuição de variáveis dos tipos primitivos
        int idade = 30;
        double altura = 1.75;
        char primeiraLetraDoNome = 'L';
        boolean estaChovendo = false;

        // Impressão dos valores no console
        System.out.println("Idade (int): " + idade);
        System.out.println("Altura (double): " + altura);
        System.out.println("Primeira letra do nome (char): " + primeiraLetraDoNome);
        System.out.println("Está chovendo? (boolean): " + estaChovendo);
    }
}